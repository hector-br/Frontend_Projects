
const verInformacion = document.getElementById('verContent-one');
verInformacion.addEventListener('click', function(event){

    let informacion = document.getElementById('p-one');
    let cambiarFlecha = document.getElementById('verContent-one');

    if(informacion.style.display === 'none' || informacion.style.display === ''){

        informacion.style.display = 'block';
        cambiarFlecha.style.transform = 'rotateX(180deg)';

    }else{

        informacion.style.display = 'none';
        cambiarFlecha.style.transform = 'rotateY(180deg)';

    }

});


